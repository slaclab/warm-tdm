        @self.command()
        def AnaPowerOff():
            self.PwrSyncB.set(1, write=False)
            self.PwrSyncC.set(0, write=False)
            self.writeBlocks()

        @self.command()
        def AnaPowerOn():
            self.PwrSyncB.set(0, write=False)
            self.PwrSyncC.set(1, write=False)
            self.writeBlocks()
        

