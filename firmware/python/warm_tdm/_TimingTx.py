import pyrogue as pr


import warm_tdm

class TimingTx(pr.Device):
    def __init__(self, **kwargs):
        super().__init__(**kwargs)

        self.add(pr.RemoteVariable(
            name = "Mode",
            description = "Select whether row-boundary events are generated by the hardware row-period timer or by the software strobe command.",
            offset = 0x18,
            bitSize = 1,
            bitOffset = 0,
            enum = {
                0: "Software",
                1: "Hardware"}))

        self.add(pr.RemoteCommand(
            name = "SoftRowStrobe",
            description = "Request a single software-driven row-boundary event when Mode is Software.",
            offset = 0x1C,
            bitSize = 1,
            bitOffset = 0,
            function = pr.RemoteCommand.touchOne))
        
